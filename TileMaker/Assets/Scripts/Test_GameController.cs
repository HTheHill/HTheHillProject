    void BuildLevel()
    {
        GameObject dynamicParent = GameObject.Find("(Test)DynamicObjects");
        
        // level 배열의 모든 요소를 도는 for문
        for (int yPos = 0; yPos < level.Length; yPos++)
        {
            for (int xPos = 0; xPos < (level[yPos].Length); xPos++)
            {
                //값이 0이면 아무것도 하지 않는다
                // 값이 1이면 벽을 생성한다.
                if (level[yPos][xPos] == 1)
                {
                    // 생성 위치
                    //  x좌표: xPos(배열에서의 열 index)
                    //  y좌표: 배열 길이 - yPos(배열에서의 행 index)
                        // 만약 0, 0이면 y좌표 == 25-0 == 25
